package com.tlf.oss.resourceinventory.schemas;

public class GeographicPoint {

	private String coordinateSystem;
    private Double coordinateX;
    private Double coordinateY;
    private Double coordinateZ;
     
    public GeographicPoint() {
 
    }
 
    public GeographicPoint(String coordinateSystem, Double coordinateX, Double coordinateY, Double coordinateZ) {
        this.coordinateSystem = coordinateSystem;
        this.coordinateX = coordinateX;
        this.coordinateY = coordinateY;
        this.coordinateZ = coordinateZ;
    }
 
    public String getCoordinateSystem() {
        return coordinateSystem;
    }
 
    public void setCoordinateSystem(String coordinateSystem) {
        this.coordinateSystem = coordinateSystem;
    }
 
    public Double getCoordinateX() {
        return coordinateX;
    }
 
    public void setCoordinateX(Double coordinateX) {
        this.coordinateX = coordinateX;
    }
 
    public Double getCoordinateY() {
        return coordinateY;
    }
 
    public void setCoordinateY(Double coordinateY) {
        this.coordinateY = coordinateY;
    }
 
    public Double getCoordinateZ() {
        return coordinateZ;
    }
 
    public void setCoordinateZ(Double coordinateZ) {
        this.coordinateZ = coordinateZ;
    }
 
    @Override
    public String toString() {
        return "GeographicPoint [coordinateX=" + coordinateX + ", coordinateY=" + coordinateY + ", coordinateZ="
                + coordinateZ + "]";
    }
}
