
package com.tlf.oss.resourceinventory.schemas;

import java.util.List;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlType;

@XmlRootElement
@XmlAccessorType(XmlAccessType.FIELD)
@XmlType(name = "Equipment", propOrder = { "typeOfResource", "name", "ip", "origin", "id", "vendor", "model", "location", "hasCards"})
public class Equipment {

	protected String typeOfResource;
	protected String name;
	protected String ip;
	protected String origin;
	protected String id;
	protected String vendor;
	protected String model;
	protected String location;
	protected List<PortAdapterCard> hasCards;
	
	/**
	 * @return the typeOfResource
	 */
	public String getTypeOfResource() {
		return typeOfResource;
	}
	
	/**
	 * @param typeOfResource the typeOfResource to set
	 */
	public void setTypeOfResource(String typeOfResource) {
		this.typeOfResource = typeOfResource;
	}
	
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	
	/**
	 * @return the profile
	 */
	public String getOrigin() {
		return origin;
	}
	
	/**
	 * @param profile the profile to set
	 */
	public void setOrigin(String origin) {
		this.origin = origin;
	}
	
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	
	/**
	 * @return the vendor
	 */
	public String getVendor() {
		return vendor;
	}
	
	/**
	 * @param vendor the vendor to set
	 */
	public void setVendor(String vendor) {
		this.vendor = vendor;
	}
	
	/**
	 * @return the model
	 */
	public String getModel() {
		return model;
	}
	
	/**
	 * @param model the model to set
	 */
	public void setModel(String model) {
		this.model = model;
	}

	/**
	 * @return the location
	 */
	public String getLocation() {
		return location;
	}
	
	/**
	 * @param location the location to set
	 */
	public void setLocation(String location) {
		this.location = location;
	}
	
	/**
	 * @return the hasCards
	 */
	public List<PortAdapterCard> getHasCards() {
		return hasCards;
	}

	/**
	 * @param hasCards the hasCards to set
	 */
	public void setHasCards(List<PortAdapterCard> hasCards) {
		this.hasCards = hasCards;
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}	
}